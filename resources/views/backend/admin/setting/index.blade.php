        $(function () {
            table = $('#manage_all').DataTable({

                processing: true,
                serverSide: true,
                ajax: '{!! route('admin.allSetting.settings') !!}',
                columns: [
                    {data: 'DT_RowIndex', name: 'DT_RowIndex'},
                    {data: 'name', name: 'name'},
                    {data: 'email', name: 'email'},
